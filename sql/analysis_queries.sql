-- Query 25: Customer Lifetime Value with CTE
-- Business Question: What is the total lifetime spend per customer?
-- SQL Concept: CTE for multi-step calculation
-- ====================================================================
WITH CUSTOMER_ORDERS AS (
    -- Step 1: Calculate customer-level metrics
    SELECT
        O.CUSTOMER_ID,
        COUNT(DISTINCT O.ORDER_ID)     AS TOTAL_ORDERS,
        SUM(O.TOTAL_AMOUNT)            AS LIFETIME_VALUE,
        MIN(O.ORDER_DATE)              AS FIRST_ORDER,
        MAX(O.ORDER_DATE)              AS LAST_ORDER
    FROM
        ORDERS O
    WHERE
        O.ORDER_STATUS != 'Cancelled'
    GROUP BY
        O.CUSTOMER_ID
),
CUSTOMER_PROFILE AS (
    -- Step 2: Join with customer master data
    SELECT
        C.CUSTOMER_ID,
        C.CUSTOMER_NAME,
        C.STATE,
        C.REGION,
        CO.TOTAL_ORDERS,
        ROUND(CO.LIFETIME_VALUE, 2)    AS CUSTOMER_LTV,
        CO.FIRST_ORDER,
        CO.LAST_ORDER,
        DATEDIFF('day', CO.FIRST_ORDER, CO.LAST_ORDER) AS ACTIVE_DAYS
    FROM
        CUSTOMERS C
        INNER JOIN CUSTOMER_ORDERS CO ON C.CUSTOMER_ID = CO.CUSTOMER_ID
)
-- Step 3: Show top customers by LTV
SELECT
    CUSTOMER_ID,
    CUSTOMER_NAME,
    STATE,
    REGION,
    TOTAL_ORDERS,
    CUSTOMER_LTV,
    FIRST_ORDER,
    LAST_ORDER,
    ACTIVE_DAYS
FROM
    CUSTOMER_PROFILE
ORDER BY
    CUSTOMER_LTV DESC
LIMIT 20;


-- Query 26: Monthly Revenue Growth Percentage
-- Business Question: How is revenue growing month-over-month?
-- SQL Concept: CTE + LAG window function for growth calculation
-- ====================================================================
WITH MONTHLY_REV AS (
    SELECT
        TO_CHAR(ORDER_DATE, 'YYYY-MM')        AS YEAR_MONTH,
        YEAR(ORDER_DATE)                       AS YEAR,
        MONTH(ORDER_DATE)                      AS MONTH,
        ROUND(SUM(TOTAL_AMOUNT), 2)            AS MONTHLY_REVENUE
    FROM
        ORDERS
    WHERE
        ORDER_STATUS != 'Cancelled'
    GROUP BY
        TO_CHAR(ORDER_DATE, 'YYYY-MM'),
        YEAR(ORDER_DATE),
        MONTH(ORDER_DATE)
),
REVENUE_WITH_GROWTH AS (
    SELECT
        YEAR_MONTH,
        MONTHLY_REVENUE,
        LAG(MONTHLY_REVENUE) OVER (ORDER BY YEAR, MONTH) AS PREV_MONTH_REVENUE
    FROM
        MONTHLY_REV
)
SELECT
    YEAR_MONTH,
    MONTHLY_REVENUE,
    PREV_MONTH_REVENUE,
    CASE
        WHEN PREV_MONTH_REVENUE IS NULL OR PREV_MONTH_REVENUE = 0
            THEN NULL
        ELSE ROUND(
            (MONTHLY_REVENUE - PREV_MONTH_REVENUE) * 100.0 / PREV_MONTH_REVENUE, 2
        )
    END AS MOM_GROWTH_PCT
FROM
    REVENUE_WITH_GROWTH
ORDER BY
    YEAR_MONTH;


-- Query 27: Top 3 Products per Category (CTE + ROW_NUMBER)
-- Business Question: Which 3 products lead revenue in each category?
-- SQL Concept: CTE with ROW_NUMBER for top-N per group
-- ====================================================================
WITH PRODUCT_REVENUE AS (
    SELECT
        P.CATEGORY,
        P.PRODUCT_NAME,
        P.BRAND,
        COUNT(DISTINCT O.ORDER_ID)          AS TOTAL_ORDERS,
        ROUND(SUM(O.TOTAL_AMOUNT), 2)       AS TOTAL_REVENUE,
        ROW_NUMBER() OVER (
            PARTITION BY P.CATEGORY
            ORDER BY SUM(O.TOTAL_AMOUNT) DESC
        )                                   AS RANK_IN_CATEGORY
    FROM
        PRODUCTS P
        JOIN ORDERS O ON P.PRODUCT_ID = O.PRODUCT_ID
    WHERE
        O.ORDER_STATUS != 'Cancelled'
    GROUP BY
        P.CATEGORY, P.PRODUCT_NAME, P.BRAND
)
SELECT
    CATEGORY,
    RANK_IN_CATEGORY,
    PRODUCT_NAME,
    BRAND,
    TOTAL_ORDERS,
    TOTAL_REVENUE
FROM
    PRODUCT_REVENUE
WHERE
    RANK_IN_CATEGORY <= 3
ORDER BY
    CATEGORY, RANK_IN_CATEGORY;



