-- SECTION 5: SUBQUERIES
-- SQL Concept: Nested SELECT inside WHERE / FROM clause
-- ====================================================================


-- Query 19: Orders Above Average Order Value
-- Business Question: Which orders are above the average order amount?
-- SQL Concept: Scalar subquery in WHERE clause
-- ====================================================================
SELECT
    O.ORDER_ID,
    C.CUSTOMER_NAME,
    P.PRODUCT_NAME,
    O.TOTAL_AMOUNT,
    ROUND((SELECT AVG(TOTAL_AMOUNT)
           FROM ORDERS
           WHERE ORDER_STATUS != 'Cancelled'), 2) AS AVG_ORDER_VALUE
FROM
    ORDERS O
    JOIN CUSTOMERS C ON O.CUSTOMER_ID = C.CUSTOMER_ID
    JOIN PRODUCTS  P ON O.PRODUCT_ID  = P.PRODUCT_ID
WHERE
    O.ORDER_STATUS != 'Cancelled'
    AND O.TOTAL_AMOUNT > (
        SELECT AVG(TOTAL_AMOUNT)
        FROM   ORDERS
        WHERE  ORDER_STATUS != 'Cancelled'
    )
ORDER BY
    O.TOTAL_AMOUNT DESC
LIMIT 20;


-- Query 20: Top Product per Category (Subquery approach)
-- Business Question: Which product generates the most revenue in each category?
-- SQL Concept: Correlated subquery
-- ====================================================================
SELECT
    P.CATEGORY,
    P.PRODUCT_NAME,
    P.BRAND,
    ROUND(SUM(O.TOTAL_AMOUNT), 2) AS CATEGORY_TOP_REVENUE
FROM
    PRODUCTS P
    JOIN ORDERS O ON P.PRODUCT_ID = O.PRODUCT_ID
WHERE
    O.ORDER_STATUS != 'Cancelled'
GROUP BY
    P.CATEGORY, P.PRODUCT_NAME, P.BRAND
HAVING
    SUM(O.TOTAL_AMOUNT) = (
        SELECT MAX(INNER_SUM.PRODUCT_REV)
        FROM (
            SELECT
                P2.CATEGORY,
                SUM(O2.TOTAL_AMOUNT) AS PRODUCT_REV
            FROM PRODUCTS P2
            JOIN ORDERS O2 ON P2.PRODUCT_ID = O2.PRODUCT_ID
            WHERE O2.ORDER_STATUS != 'Cancelled'
            GROUP BY P2.CATEGORY, P2.PRODUCT_ID
        ) INNER_SUM
        WHERE INNER_SUM.CATEGORY = P.CATEGORY
    )
ORDER BY
    P.CATEGORY;


-- Query 21: States with Above-Average Customer Count
-- Business Question: Which states have more customers than the national average?
-- SQL Concept: Subquery in HAVING clause
-- ====================================================================
SELECT
    STATE,
    COUNT(DISTINCT CUSTOMER_ID) AS CUSTOMER_COUNT
FROM
    CUSTOMERS
GROUP BY
    STATE
HAVING
    COUNT(DISTINCT CUSTOMER_ID) > (
        SELECT AVG(STATE_COUNT)
        FROM (
            SELECT COUNT(DISTINCT CUSTOMER_ID) AS STATE_COUNT
            FROM   CUSTOMERS
            GROUP BY STATE
        ) STATE_SUMMARY
    )
ORDER BY
    CUSTOMER_COUNT DESC;



