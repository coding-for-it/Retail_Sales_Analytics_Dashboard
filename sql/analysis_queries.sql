-- Query 01: Total Revenue
-- Business Question: What is the total revenue generated by the business?
-- ====================================================================
SELECT
    ROUND(SUM(TOTAL_AMOUNT), 2)   AS TOTAL_REVENUE_INR
FROM
    ORDERS
WHERE
    ORDER_STATUS != 'Cancelled';


-- Query 02: Total Orders Placed
-- Business Question: How many orders have been placed in total?
-- ====================================================================
SELECT
    COUNT(DISTINCT ORDER_ID)   AS TOTAL_ORDERS
FROM
    ORDERS;


-- Query 03: Total Unique Customers
-- Business Question: How many unique customers have placed at least one order?
-- ====================================================================
SELECT
    COUNT(DISTINCT CUSTOMER_ID)   AS TOTAL_CUSTOMERS_WITH_ORDERS
FROM
    ORDERS;


-- Query 04: Total Products in Catalog
-- Business Question: How many products are available in the system?
-- ====================================================================
SELECT
    COUNT(DISTINCT PRODUCT_ID)   AS TOTAL_PRODUCTS,
    COUNT(DISTINCT CATEGORY)     AS TOTAL_CATEGORIES
FROM
    PRODUCTS;


-- Query 05: Average Order Value (AOV)
-- Business Question: What is the average revenue per order?
-- AOV = Total Revenue / Total Orders (excluding cancelled)
-- ====================================================================
SELECT
    COUNT(DISTINCT ORDER_ID)        AS TOTAL_ORDERS,
    ROUND(SUM(TOTAL_AMOUNT), 2)     AS TOTAL_REVENUE,
    ROUND(AVG(TOTAL_AMOUNT), 2)     AS AVG_ORDER_VALUE
FROM
    ORDERS
WHERE
    ORDER_STATUS != 'Cancelled';



