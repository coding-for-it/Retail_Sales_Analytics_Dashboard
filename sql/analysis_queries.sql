-- Query 28: Product Revenue Ranking using RANK()
-- Business Question: Rank all products by total revenue (gaps for ties)
-- SQL Concept: RANK() — skips rank numbers after ties
-- ====================================================================
SELECT
    P.PRODUCT_NAME,
    P.CATEGORY,
    P.BRAND,
    ROUND(SUM(O.TOTAL_AMOUNT), 2)    AS TOTAL_REVENUE,
    RANK() OVER (
        ORDER BY SUM(O.TOTAL_AMOUNT) DESC
    )                                AS REVENUE_RANK
FROM
    PRODUCTS P
    JOIN ORDERS O ON P.PRODUCT_ID = O.PRODUCT_ID
WHERE
    O.ORDER_STATUS != 'Cancelled'
GROUP BY
    P.PRODUCT_NAME, P.CATEGORY, P.BRAND
ORDER BY
    REVENUE_RANK;


-- Query 29: Customer Spending Rank using DENSE_RANK()
-- Business Question: Rank customers by spend (no gaps for ties)
-- SQL Concept: DENSE_RANK() — consecutive rank numbers even with ties
-- ====================================================================
SELECT
    C.CUSTOMER_NAME,
    C.STATE,
    C.REGION,
    ROUND(SUM(O.TOTAL_AMOUNT), 2)   AS TOTAL_SPENT,
    DENSE_RANK() OVER (
        ORDER BY SUM(O.TOTAL_AMOUNT) DESC
    )                               AS SPENDING_RANK
FROM
    CUSTOMERS C
    JOIN ORDERS O ON C.CUSTOMER_ID = O.CUSTOMER_ID
WHERE
    O.ORDER_STATUS != 'Cancelled'
GROUP BY
    C.CUSTOMER_NAME, C.STATE, C.REGION
ORDER BY
    SPENDING_RANK
LIMIT 25;


-- Query 30: Order Sequence per Customer using ROW_NUMBER()
-- Business Question: What was the 1st, 2nd, 3rd order placed by each customer?
-- SQL Concept: ROW_NUMBER() PARTITION BY customer — sequential numbering
-- ====================================================================
SELECT
    O.CUSTOMER_ID,
    C.CUSTOMER_NAME,
    O.ORDER_ID,
    O.ORDER_DATE,
    O.TOTAL_AMOUNT,
    ROW_NUMBER() OVER (
        PARTITION BY O.CUSTOMER_ID
        ORDER BY O.ORDER_DATE
    ) AS ORDER_SEQUENCE
FROM
    ORDERS O
    JOIN CUSTOMERS C ON O.CUSTOMER_ID = C.CUSTOMER_ID
WHERE
    O.ORDER_STATUS != 'Cancelled'
ORDER BY
    O.CUSTOMER_ID, ORDER_SEQUENCE
LIMIT 50;


-- Query 31: Customer Revenue Quartile Segmentation using NTILE()
-- Business Question: Split customers into 4 equal revenue quartiles.
-- SQL Concept: NTILE(4) divides the result set into 4 equal buckets
-- ====================================================================
WITH CUSTOMER_SPEND AS (
    SELECT
        C.CUSTOMER_ID,
        C.CUSTOMER_NAME,
        C.STATE,
        ROUND(SUM(O.TOTAL_AMOUNT), 2) AS TOTAL_SPENT
    FROM
        CUSTOMERS C
        JOIN ORDERS O ON C.CUSTOMER_ID = O.CUSTOMER_ID
    WHERE
        O.ORDER_STATUS != 'Cancelled'
    GROUP BY
        C.CUSTOMER_ID, C.CUSTOMER_NAME, C.STATE
)
SELECT
    CUSTOMER_ID,
    CUSTOMER_NAME,
    STATE,
    TOTAL_SPENT,
    NTILE(4) OVER (ORDER BY TOTAL_SPENT DESC) AS REVENUE_QUARTILE,
    CASE NTILE(4) OVER (ORDER BY TOTAL_SPENT DESC)
        WHEN 1 THEN 'Top 25%'
        WHEN 2 THEN 'Upper Middle 25%'
        WHEN 3 THEN 'Lower Middle 25%'
        WHEN 4 THEN 'Bottom 25%'
    END AS QUARTILE_LABEL
FROM
    CUSTOMER_SPEND
ORDER BY
    TOTAL_SPENT DESC
LIMIT 30;


-- Query 32: Category-Level Product Ranking (RANK PARTITION BY)
-- Business Question: Rank products within each category by revenue.
-- SQL Concept: RANK() with PARTITION BY — separate ranking per group
-- ====================================================================
SELECT
    P.CATEGORY,
    P.PRODUCT_NAME,
    P.BRAND,
    ROUND(SUM(O.TOTAL_AMOUNT), 2)   AS PRODUCT_REVENUE,
    RANK() OVER (
        PARTITION BY P.CATEGORY
        ORDER BY SUM(O.TOTAL_AMOUNT) DESC
    )                               AS RANK_IN_CATEGORY
FROM
    PRODUCTS P
    JOIN ORDERS O ON P.PRODUCT_ID = O.PRODUCT_ID
WHERE
    O.ORDER_STATUS != 'Cancelled'
GROUP BY
    P.CATEGORY, P.PRODUCT_NAME, P.BRAND
ORDER BY
    P.CATEGORY, RANK_IN_CATEGORY;


-- Query 33: Bottom 10 Products (Lowest Revenue)
