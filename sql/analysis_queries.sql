    O.ORDER_STATUS != 'Cancelled'
GROUP BY
    C.CUSTOMER_ID, C.CUSTOMER_NAME, C.CITY, C.STATE
HAVING
    SUM(O.TOTAL_AMOUNT) > 50000
ORDER BY
    TOTAL_SPENT DESC;


