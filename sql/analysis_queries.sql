-- Query 37: Repeat vs One-Time Customer Analysis
-- Business Question: What percentage of customers are repeat buyers?
-- SQL Concept: CASE WHEN + COUNT + percentage
-- ====================================================================
WITH CUSTOMER_ORDER_COUNT AS (
    SELECT
        CUSTOMER_ID,
        COUNT(DISTINCT ORDER_ID) AS ORDER_COUNT
    FROM
        ORDERS
    WHERE
        ORDER_STATUS != 'Cancelled'
    GROUP BY
        CUSTOMER_ID
)
SELECT
    COUNT(CUSTOMER_ID)                                 AS TOTAL_CUSTOMERS,
    COUNT(CASE WHEN ORDER_COUNT > 1 THEN 1 END)       AS REPEAT_CUSTOMERS,
    COUNT(CASE WHEN ORDER_COUNT = 1 THEN 1 END)       AS ONE_TIME_CUSTOMERS,
    ROUND(
        COUNT(CASE WHEN ORDER_COUNT > 1 THEN 1 END)
        * 100.0 / COUNT(CUSTOMER_ID), 2
    )                                                  AS REPEAT_CUSTOMER_PCT
FROM
    CUSTOMER_ORDER_COUNT;


-- Query 38: Customer Segmentation by Spending (CASE WHEN)
-- Business Question: How are our customers distributed across spending tiers?
-- SQL Concept: CASE WHEN for conditional bucket assignment
-- ====================================================================
SELECT
    CASE
        WHEN TOTAL_SPENT >= 500000 THEN 'Platinum (>= 5 Lakh)'
        WHEN TOTAL_SPENT >= 200000 THEN 'Gold (2–5 Lakh)'
        WHEN TOTAL_SPENT >= 50000  THEN 'Silver (50K–2 Lakh)'
        ELSE                            'Bronze (< 50K)'
    END                                                AS SEGMENT,
    COUNT(CUSTOMER_ID)                                 AS CUSTOMER_COUNT,
    ROUND(SUM(TOTAL_SPENT), 2)                         AS SEGMENT_REVENUE,
    ROUND(AVG(TOTAL_SPENT), 2)                         AS AVG_SPEND
FROM (
    SELECT
        C.CUSTOMER_ID,
        SUM(O.TOTAL_AMOUNT) AS TOTAL_SPENT
    FROM
        CUSTOMERS C
        JOIN ORDERS O ON C.CUSTOMER_ID = O.CUSTOMER_ID
    WHERE
        O.ORDER_STATUS != 'Cancelled'
    GROUP BY
        C.CUSTOMER_ID
) CUSTOMER_TOTALS
GROUP BY
    SEGMENT
ORDER BY
    SEGMENT_REVENUE DESC;


-- Query 39: Revenue Contribution % by Category
-- Business Question: What share of total revenue does each category contribute?
-- SQL Concept: SUM OVER () for total + percentage calculation
-- ====================================================================
SELECT
    P.CATEGORY,
    ROUND(SUM(O.TOTAL_AMOUNT), 2)           AS CATEGORY_REVENUE,
    ROUND(
        SUM(O.TOTAL_AMOUNT) * 100.0 /
        SUM(SUM(O.TOTAL_AMOUNT)) OVER (), 2
    )                                        AS REVENUE_CONTRIBUTION_PCT,
    RANK() OVER (ORDER BY SUM(O.TOTAL_AMOUNT) DESC) AS CATEGORY_RANK
FROM
    ORDERS O
    JOIN PRODUCTS P ON O.PRODUCT_ID = P.PRODUCT_ID
WHERE
    O.ORDER_STATUS != 'Cancelled'
GROUP BY
    P.CATEGORY
ORDER BY
    REVENUE_CONTRIBUTION_PCT DESC;


-- Query 40: Comprehensive Sales Dashboard Summary
-- Business Question: Single query for executive dashboard overview.
-- SQL Concept: Combining multiple metrics into one row using subqueries
-- ====================================================================
SELECT
    (SELECT COUNT(DISTINCT CUSTOMER_ID)
     FROM CUSTOMERS)                                            AS TOTAL_REGISTERED_CUSTOMERS,

    (SELECT COUNT(DISTINCT CUSTOMER_ID)
     FROM ORDERS
     WHERE ORDER_STATUS != 'Cancelled')                         AS ACTIVE_CUSTOMERS,

    (SELECT COUNT(DISTINCT PRODUCT_ID)
     FROM PRODUCTS)                                             AS TOTAL_PRODUCTS,

    (SELECT COUNT(DISTINCT ORDER_ID)
     FROM ORDERS)                                               AS TOTAL_ORDERS,

    (SELECT COUNT(DISTINCT ORDER_ID)
     FROM ORDERS
     WHERE ORDER_STATUS = 'Completed')                          AS COMPLETED_ORDERS,

    (SELECT ROUND(SUM(TOTAL_AMOUNT), 2)
     FROM ORDERS
     WHERE ORDER_STATUS = 'Completed')                          AS TOTAL_REVENUE_INR,

    (SELECT ROUND(AVG(TOTAL_AMOUNT), 2)
     FROM ORDERS
     WHERE ORDER_STATUS != 'Cancelled')                         AS AVG_ORDER_VALUE,

    (SELECT COUNT(DISTINCT CUSTOMER_ID)
     FROM (SELECT CUSTOMER_ID
           FROM ORDERS
           WHERE ORDER_STATUS != 'Cancelled'
           GROUP BY CUSTOMER_ID
           HAVING COUNT(DISTINCT ORDER_ID) > 1))                AS REPEAT_CUSTOMERS;
