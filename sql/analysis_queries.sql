-- Query 34: Cumulative Revenue (Running Total by Month)
-- Business Question: How does revenue accumulate over the year?
-- SQL Concept: SUM() OVER (ORDER BY ...) — running total
-- ====================================================================
WITH MONTHLY AS (
    SELECT
        TO_CHAR(ORDER_DATE, 'YYYY-MM')    AS YEAR_MONTH,
        YEAR(ORDER_DATE)                   AS YEAR,
        MONTH(ORDER_DATE)                  AS MONTH,
        ROUND(SUM(TOTAL_AMOUNT), 2)        AS MONTHLY_REVENUE
    FROM
        ORDERS
    WHERE
        ORDER_STATUS != 'Cancelled'
    GROUP BY
        TO_CHAR(ORDER_DATE, 'YYYY-MM'),
        YEAR(ORDER_DATE),
        MONTH(ORDER_DATE)
)
SELECT
    YEAR_MONTH,
    MONTHLY_REVENUE,
    SUM(MONTHLY_REVENUE) OVER (
        ORDER BY YEAR, MONTH
        ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW
    )                                     AS CUMULATIVE_REVENUE
FROM
    MONTHLY
ORDER BY
    YEAR, MONTH;


-- Query 35: 3-Month Moving Average Revenue
-- Business Question: What is the smoothed revenue trend (removes seasonality)?
-- SQL Concept: AVG OVER with ROWS BETWEEN frame — sliding window
-- ====================================================================
WITH MONTHLY AS (
    SELECT
        TO_CHAR(ORDER_DATE, 'YYYY-MM')    AS YEAR_MONTH,
        YEAR(ORDER_DATE)                   AS YEAR,
        MONTH(ORDER_DATE)                  AS MONTH,
        ROUND(SUM(TOTAL_AMOUNT), 2)        AS MONTHLY_REVENUE
    FROM
        ORDERS
    WHERE
        ORDER_STATUS != 'Cancelled'
    GROUP BY
        TO_CHAR(ORDER_DATE, 'YYYY-MM'),
        YEAR(ORDER_DATE),
        MONTH(ORDER_DATE)
)
SELECT
    YEAR_MONTH,
    MONTHLY_REVENUE,
    ROUND(AVG(MONTHLY_REVENUE) OVER (
        ORDER BY YEAR, MONTH
        ROWS BETWEEN 2 PRECEDING AND CURRENT ROW
    ), 2)                                 AS MOVING_AVG_3_MONTHS
FROM
    MONTHLY
ORDER BY
    YEAR, MONTH;


-- Query 36: Running Order Count per Customer (ROW_NUMBER as Running Count)
-- Business Question: Track the order journey of individual customers.
-- SQL Concept: ROW_NUMBER() as a running counter partitioned by customer
-- ====================================================================
SELECT
    O.CUSTOMER_ID,
    C.CUSTOMER_NAME,
    O.ORDER_ID,
    O.ORDER_DATE,
    O.TOTAL_AMOUNT,
    O.ORDER_STATUS,
    ROW_NUMBER() OVER (
        PARTITION BY O.CUSTOMER_ID
        ORDER BY O.ORDER_DATE
    )                                    AS NTH_ORDER,
    SUM(O.TOTAL_AMOUNT) OVER (
        PARTITION BY O.CUSTOMER_ID
        ORDER BY O.ORDER_DATE
        ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW
    )                                    AS RUNNING_CUSTOMER_SPEND
FROM
    ORDERS O
    JOIN CUSTOMERS C ON O.CUSTOMER_ID = C.CUSTOMER_ID
ORDER BY
    O.CUSTOMER_ID, NTH_ORDER
LIMIT 50;



