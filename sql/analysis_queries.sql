-- Query 11: High-Value Customers (Spent > 50,000 INR)
-- Business Question: Which customers have spent more than 50,000 rupees?
-- SQL Concept: HAVING filters after GROUP BY (WHERE filters before)
-- ====================================================================
SELECT
    C.CUSTOMER_ID,
    C.CUSTOMER_NAME,
    C.CITY,
    C.STATE,
    COUNT(DISTINCT O.ORDER_ID)          AS TOTAL_ORDERS,
    ROUND(SUM(O.TOTAL_AMOUNT), 2)       AS TOTAL_SPENT
